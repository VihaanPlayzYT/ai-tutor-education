# VECTORIZER — tuned for larger, more diverse dataset
# =============================================================================
vectorizer = TfidfVectorizer(
    stop_words='english',
    ngram_range=(1, 3),       # unigrams + bigrams + trigrams
    max_features=20000,       # increased from 10000
    min_df=1,                 # keep all vocabulary (larger dataset)
    sublinear_tf=True,        # log-scale TF to reduce impact of common terms
)

X_vec = vectorizer.fit_transform(df['question'])

# =============================================================================
# MODEL — class_weight='balanced' handles any remaining imbalance automatically
# =============================================================================
